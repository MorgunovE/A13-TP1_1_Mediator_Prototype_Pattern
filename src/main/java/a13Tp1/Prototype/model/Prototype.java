package a13Tp1.Prototype.model;

/**
 * Interface for cloning prototypes.
 * Provides a method to clone the prototype.
 *
 * @author Evgenii Morgunov
 * @version 1.0
 * @since 28-11-2024
 */
public interface Prototype {
    /**
     * Clones the prototype.
     *
     * @return a clone of the prototype
     */
    Prototype clone();
}